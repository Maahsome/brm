package cmd

import (
	"fmt"
	"os"

	"github.com/spf13/cobra"

	homedir "github.com/mitchellh/go-homedir"
	"github.com/spf13/viper"
)

var cfgFile string

var rootCmd = &cobra.Command{
	Use:   "brm",
	Short: "A simple CLI tool to manage applications with binary releases",
	Long: `Package managers are useful, handle complicated dependency installations, and all that
good stuff.  With many new tools being released as binary installations through the "Release"
feature of github and gitlab, it is nice to have a tool that simply goes to the source artifacts
and downloads the binary and symlinks it into the path.  Allowing you to quickly switch between
multiple versions of any supported tool.`,
}

func Execute() {
	if err := rootCmd.Execute(); err != nil {
		fmt.Println(err)
		os.Exit(1)
	}
}

func init() {
	cobra.OnInitialize(initConfig)

	rootCmd.PersistentFlags().StringVar(&cfgFile, "config", "", "config file (default is $HOME/.config/brm/brm.yaml)")
}

// initConfig reads in config file and ENV variables if set.
func initConfig() {
	if cfgFile != "" {
		// Use config file from the flag.
		viper.SetConfigFile(cfgFile)
	} else {
		// Find home directory.
		home, err := homedir.Dir()
		if err != nil {
			fmt.Println(err)
			os.Exit(1)
		}

		// Search config in home directory with name ".brm" (without extension).
		viper.AddConfigPath(home)
		viper.AddConfigPath("$HOME/.config/brm")
		viper.SetConfigName("brm")
		viper.SetConfigType("yaml")
	}

	viper.AutomaticEnv() // read in environment variables that match

	// If a config file is found, read it in.
	if err := viper.ReadInConfig(); err == nil {
		fmt.Println("Using config file:", viper.ConfigFileUsed())
	}
}
